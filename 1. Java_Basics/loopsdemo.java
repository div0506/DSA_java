
public class loopsdemo 
{
    public static void main(String[] args)
    {   
        
        //for loop
        for( int i=0;i<11;i++)
        {
            System.out.println(i+1);
        }

        //while loop
        int j=0;
        while( j<11)
        {
            System.out.println(j+1);
            j++;
        }

        //do while loop
        int k =0;
        do{
            System.out.println(k+1);
            k++;
        }
        while(k<11);
    }
    
}
